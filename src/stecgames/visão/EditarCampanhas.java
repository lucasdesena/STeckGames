public void recebendo(String recebe) throws ServerException{
    int recebeAux = Integer.parseInt(recebe);
    Campanha c  = new Campanha();
    
    try{
     
    c.setId_campanhas(recebeAux);
    c = CampanhaDAO.Buscar(recebeAux);
    
 }catch(Exception e){
     
    throw new ServerException(e.getMessage());
    
 }
    String nome = c.getNome();
    String tipo = c.getTipo();
    String valor = c.getValor();
    String descricao = c.getDescricao();
    String link = c.getLink();
    int id = c.getId_campanhas();
    
    txtId.setText(String.valueOf(id));       
    txtNome.setText(nome);
    boxTipo.setSelectedItem(tipo);
    txtValor.setText(valor);
    txtDescricao.setText(descricao);
    txtLink.setText(link);
    
        
    
    
    
    }


