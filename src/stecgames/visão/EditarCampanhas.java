    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
          
        String idaux = txtId.getText();
        int id = Integer.parseInt(idaux);
        
        String nome = txtNome.getText().trim();
        String tipo = boxTipo.getSelectedItem().toString();
        String valor = txtValor.getText();
        String descricao = txtDescricao.getText();
        String link = txtLink.getText();
        
        String msgErro;

        if (nome.equals("")) {
            msgErro = "Insira o NOME da campanha antes de continuar.";
            JOptionPane.showMessageDialog(null, msgErro);
        } else if (tipo.equals("")) {
                msgErro = "Insira o TIPO da campanha antes de continuar.";
                JOptionPane.showMessageDialog(null, msgErro);
            } else  if (valor.equals("")) {
                    msgErro = "Insira o VALOR da campanha antes de continuar.";
                    JOptionPane.showMessageDialog(null, msgErro);
                } else if (link.equals("")) {
                        msgErro = "Insira o LINK antes de continuar.";
                        JOptionPane.showMessageDialog(null, msgErro);
                    } 
                       Campanha campanha = new Campanha();
                       
         campanha.setNome(nome);        
         campanha.setTipo(tipo);
         campanha.setValor(valor);
         campanha.setDescricao(descricao);
         campanha.setLink(link);
         campanha.setId_campanhas(id);
         
          CampanhaDAO campanhaDAO = new CampanhaDAO();
        boolean resultado;

        try {
            campanhaDAO.atualizar (campanha);
            JOptionPane.showMessageDialog(null, "Campanha editada com sucesso!", "Inclusão", JOptionPane.INFORMATION_MESSAGE); 
           this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(EditarCampanhas.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(EditarCampanhas.class.getName()).log(Level.SEVERE, null, ex);
        }
    
         
         
         
        
        
        
        
        
    }//GEN-LAST:event_btEditarActionPerformed

